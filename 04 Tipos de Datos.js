/*
    Tipos de Datos en JS (Primitivos)

    Number -> Almacena valores Numéricos --> 10, 20, -90
    String -> Almacena cadenas de Caracteres --> "Hola", 'Saludos', `Adios`
    Boolean -> Almacena valores Lógicos --> true, false
    Null -> Ausencia de la referencia de un objeto --> null
    Undefined -> Ausencia de Valor --> undefined

    Operadores

    typeof -> Sirve para conocer el tipo de dato
*/

// Number
let miEntero = 10;
console.log(miEntero);
console.log(typeof miEntero);

let miFlotante = 7.15;
console.log(miFlotante);
console.log(typeof miFlotante);

// String

let miCadena = "Hola";
console.log(miCadena);
console.log(typeof miCadena);

let miCadena2 = 'Mundo'
console.log(miCadena2);
console.log(typeof miCadena2);

let miCadena3 = `Variables`;
console.log(miCadena3)
console.log(typeof miCadena3);

// Boolean

let valorVerdadero = true;
console.log(valorVerdadero);
console.log(typeof valorVerdadero);

let valorFalso = false;
console.log(valorFalso);
console.log(typeof valorFalso);

// Null

let miNull = null;
console.log(miNull);
console.log(typeof miNull);

// Undefined

let miUndefined = undefined;
console.log(miUndefined);
console.log(typeof miUndefined);
